x(1)=5; % ??????? ?????
y(1)=10;

T0=0; % ??????? ?? ???????? ?? ??????????
Tf=25; % ???? ?? ???????? ?? ??????????

t(1)=0; % ??????? ????? ?? ????? 

T=0.01; % ????? ?? ??????????/???????????

n=floor((Tf-T0)/T); % n ?????? ?? ??????????

f1=@(x,y) x*(6-2*x-y); % ?????????? ?? ?????????? ??????? ?? ??????????
f2=@(x,y) y*(4-x-y);

for k=1:n-1
   
   K11=x`
   K12=f2(x(k),t(k));
   
   
   K21=f1(x(k)+(1/2)*T*K11,y(k)+(1/2)*T*K12);
   K22=f2(x(k)+(1/2)*T*K11,y(k)+(1/2)*T*K12);
   
   K31=f1(x(k)+(1/2)*T*K21,y(k)+(1/2)*T*K22);
   K32=f2(x(k)+(1/2)*T*K21,y(k)+(1/2)*T*K22);
   
   K41=f1(x(k)+T*K31,y(k)+(1/2)*T*K32);
   K42=f2(x(k)+T*K31,y(k)+(1/2)*T*K32);
    
   x(k+1)=x(k)+(1/6)*T*(K11+2*K21+2*K31+K41);
   y(k+1)=y(k)+(1/6)*T*(K12+2*K22+2*K32+K42);
   
   t(k+1)=t(k)+T;   
end

